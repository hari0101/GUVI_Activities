package guvitask4;

public class Question8 
{

//Write a program to print "Welcome to Guvi" 10 times.
	
	
	public static void main(String[] args) {

	//Initialize the String variable.
	String name = "Welcome to Guvi";
	
	//As we know the number of iteration we using for loop to execute the program.
	
	for(int i=1;i<=10;i++)
	{
		//Output the name of the variable.
		System.out.println(i + ") " + name);
	}
	
	}

}
